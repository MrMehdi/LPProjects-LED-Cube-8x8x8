#include "DELAY.h"

void Delay_ms (unsigned int ms) {
	int i=0;
	
	for (i=0; i<(25000 * ms); i++);
}

void Delay_us (unsigned int us) {
	int i=0;
	
	for (i=0; i<(25 * us); i++);
}

